# Defining Functions in Python
# Functions help in reusability and modularity in Python.
def greet(name):
    return f"Hello, {name}!"
 
print(greet("Dolly"))

def average(a,b,c):
    return (a+b+c)/3.0
    
print(average(3,5,2))  

# Defined using def keyword.
# Function name should be meaningful.
# Use return to send a value back.